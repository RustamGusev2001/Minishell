#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	void			*p;
	unsigned char	ch;

	i = 0;
	p = (void *)s;
	ch = (unsigned char) c;
	while (i < n)
	{
		if (*(unsigned char *)(p + i) == ch)
			return (p + i);
		i++;
	}
	return (0);
}
